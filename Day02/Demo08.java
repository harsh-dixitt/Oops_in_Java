
public class Demo08 {

	public static void main(String[] args) {
		int n1 = 10; // stack
		// converting the value of primitive(value) type
		// into non primitive(Reference) type is called as boxing
		
		Integer i1 = new Integer(n1);//boxing
		

		// If the conversion between the value type
		// to refrence type is done automatically
		// then it is called as Autoboxing
		
		Integer i2 = n1; //Auto-boxing
		
		System.out.println("Hello...");
		

	}

}
